
package millonario;

/**
 *
 * @author PEREIRA
 */
public class ListadoTime {
    String nomjugadores;
    int time;

    public ListadoTime() {
    }

    public ListadoTime(String nomjugadores, int time) {
        this.nomjugadores = nomjugadores;
        this.time = time;
    }

    public String getNomjugadores() {
        return nomjugadores;
    }

    public void setNomjugadores(String nomjugadores) {
        this.nomjugadores = nomjugadores;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
    
    
    
}
